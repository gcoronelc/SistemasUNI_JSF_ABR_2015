	 private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
		 try {
			 Alumno alu = new Alumno();
			 alu.setApellido(this.txtApellido.getText());
			 alu.setNombre(this.txtNombre.getText());
			 alu.setNota(Integer.parseInt(this.txtNota.getText()));
			 if (this.id == null) {
				 dao.create(alu);
			 } else {
				 alu.setId(id);
				 dao.update(alu);
			 }
			 util.Util.showMessage(this, "Proceso ejecutado correctamente.");
			 if( this.id == null ){
				 cleanForm();
			 }
		 } catch (Exception e) {
			 util.Util.showError(this, e.getMessage());
		 }
	 }//GEN-LAST:event_btnGrabarActionPerformed

