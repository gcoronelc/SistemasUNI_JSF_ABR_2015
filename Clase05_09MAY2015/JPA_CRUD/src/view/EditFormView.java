	private void initForm() throws Exception {
		if (this.id == null) {
			this.setTitle("NUEVO ALUMNO");
			cleanForm();
		} else {
			this.setTitle("EDITAR ALUMNO " + this.id);
			loadAlumno();
		}
	}

	private void cleanForm() {
		this.txtApellido.setText("");
		this.txtNombre.setText("");
		this.txtNota.setText("");
		this.txtApellido.grabFocus();
	}

	private void loadAlumno() throws Exception {
		Alumno alu = dao.read(this.id);
		this.txtApellido.setText(alu.getApellido());
		this.txtNombre.setText(alu.getNombre());
		this.txtNota.setText(String.valueOf(alu.getNota()));
		this.txtApellido.grabFocus();
	}
